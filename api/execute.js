// Load registry (Node.js context)
let ShadowAIRegistry;
try {
  ShadowAIRegistry = require('../ai-tool-registry.js');
} catch(e) {
  // Fallback for browser context
  ShadowAIRegistry = window.ShadowAIRegistry;
}

/**
 * Main execution handler
 * @param {Object} request - Execution request
 * @param {string} transport - Transport method: 'http' | 'websocket' | 'postMessage'
 * @returns {Promise<Object>} Execution result
 */
async function executeTool(request, transport = 'http') {
  const { toolId, params, requestId } = request;

  // Find tool in registry
  const tool = ShadowAIRegistry.tools.find(t => t.id === toolId);
  if (!tool) {
    return { error: `Tool "${toolId}" not found`, requestId };
  }

  // Dispatch to specific execution engine based on tool type
  try {
    const result = await dispatchExecution(tool, params, transport);
    return { success: true, result, requestId };
  } catch (err) {
    return { error: err.message, requestId };
  }
}

/**
 * Dispatch execution to appropriate engine
 */
async function dispatchExecution(tool, params, transport) {
  switch (tool.id) {
    // Visual tools
    case 'visual-editor':
      return executeVisualEditor(params);
    case 'cursor-studio':
      return executeCursorStudio(params);
    case 'sprite-mapper':
      return executeSpriteMapper(params);
    case 'animated-exporter':
      return executeAnimatedExporter(params);
    case 'mesh-gradient':
      return executeMeshGradient(params);
    case 'css-effect':
      return executeCSSEffect(params);
    case 'color-palette':
      return executeColorPalette(params);
    
    // Audio tools
    case 'audio-synth':
      return executeAudioSynth(params);
    case 'text-to-soundscape':
      return executeTextToSoundscape(params);
    
    // AI tools
    case 'ai-prompt-builder':
      return executePromptBuilder(params);
    case 'plugin-installer':
      return executePluginInstaller(params);
    
    default:
      throw new Error(`Execution handler for "${tool.id}" not implemented yet`);
  }
}

