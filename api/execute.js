async function executeCSSEffect(params) {
  const { type, color = '#7c4dff', intensity = 0.5 } = params;
  
  let css = '';
  if (type === 'glassmorphism') {
    css = `.glass { background: rgba(255,255,255,0.1); backdrop-filter: blur(10px); border: 1px solid rgba(255,255,255,0.2); }`;
  } else if (type === 'glow') {
    css = `.glow { box-shadow: 0 0 ${20 * intensity}px ${color}; }`;
  }
  
  return { css, preview: `<div style="padding:20px;background:${color};color:white;border-radius:8px;">Preview</div>` };
}

async function executeColorPalette(params) {
  const { method, baseColor, count = 5 } = params;
  
  // Generate colors based on method
  let colors = [];
  if (method === 'random') {
    for (let i = 0; i < count; i++) {
      colors.push('#' + Math.floor(Math.random() * 16777215).toString(16).padStart(6, '0'));
    }
  } else if (method === 'monochromatic' && baseColor) {
    for (let i = 0; i < count; i++) {
      colors.push(adjustColor(baseColor, (i - count/2) * 10));
    }
  }
  
  return { colors, css: `:root { ${colors.map((c, i) => `--color-${i}: ${c};`).join(' ')} }` };
}

